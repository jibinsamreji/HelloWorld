# Addition & Subtraction
print(10+3)
print(10-3)
# Division
print(10/3)  # we get floating point
print(10//3)  # we get an integer
print(10%3)    # we get remainder

# Exponent / Power

print(10**3)

# Augmented assignment
x = 10
x = x + 3
print(x)

x = 10
x += 3  # augmented assignment operator
print(x)

# Order of Operations (Operator Precedence)
"1. Parenthesis"
"2. Exponentiation 2**3"
"3. Multiplication or Division"
"4. Addition or Subtraction"

cal = 10 + 3 * 2 ** 2
print(cal)
